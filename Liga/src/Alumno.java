
public class Alumno {
	
	String nombre;
	float estatura;
	float peso;
	
	Alumno(){
		
		this.nombre="";
		this.estatura=0;
		this.peso=0;
		
	}
	
	Alumno (String nombre, float estatura, float peso) {

			this.nombre = nombre;
			this.estatura = estatura;
			this.peso = peso;
	
	}
}